						<p>While it seems like a quick easy process to use your credit card or debit card to pay for goods or services, the process behind it is actually very complex.
						</p>
						<p>Information passes from the cardholder to the processing company of the merchant (your business) and the credit card network of the cardholder's financial institution. The bank either approves or declines the credit card transaction. Then information reverses to flow back through the same channels to the merchant to let them know if the transaction was approved or not.</p>

						<h2 class="h4">The Credit Card Processing Workflow</h2>
						<p>Credit card processing is a workflow involving a handful of steps to get the cardholder's payment and authorization information from one point to another. The end goal is to verify the buyer has the money to complete the purchase with the merchant. Once it's authorized, your business can get paid.</p>
						<ul>
							<li><span class="no">1. Cardholder:</span>  The workflow begins with the cardholder who swipes or inserts their card. The action gives payment information to the merchant physically or via the internet.</li>
							<li><span class="no">2. Merchant:</span>  The merchant takes the payment information they're given in person using POS systems. If the card and cardholder aren't present, the merchant will use an online gateway to collect cardholder information.</li>
							<li><span class="no">3. Credit card processor:</span>  The processor's role is to harvest the payment information and route it to the appropriate channels. The processor serves as a communications hub between the different parties involved in the process. Their part in the process starts with sending the payment information to the appropriate credit card network.</li>
							<li><span class="no">4. Credit card networks:</span>  The credit card networks include MasterCard, Visa, and American Express among others. Once the payment information is received from the processor, it's transmitted to the cardholder's financial institution. </li>
							<li><span class="no">5. Cardholder's financial institution:</span>  The cardholder's bank receives the payment request. They verify that the funds or credit is available on their account to complete the transaction. Often the financial institution uses extra security checks to ensure transactions are legitimate and not fraudulent. The decision transmits through the network. If funds are available, the transaction goes through. There are many different reasons any transaction may decline including insufficient funds, an exceeded credit limit, or suspicions of fraud.</li>
							<li><span class="no">6. Merchant revisited:</span>  The final part of the workflow is the decision sent back to the merchant informing them the transaction was either approved or declined. Card-present transactions will often show "approved" or "declined" on the card reader. If approved, the merchant completes the transaction and provides the promised goods or services.</li>
						</ul>
						<p>At the end of the workflow outlined above, the funds aren't immediately released from the financial institution so the transaction isn't finalized. The release of funds is a separate process that can take a few days to complete depending on the card network.</p>


						<h2 class="h4">Clearing and Settlement for Credit Cards </h2>
						<p>The release of funds is the second part of processing credit cards known as clearing and settlement. Once your small business's transactions are authorized, you send a batch of them to your processor for settlement at the end of the day. </p>
						<p>The authorizations are reconciled by the processor who then sends the batch on the credit card association networks. The processor is also the one responsible for depositing the funds from the transactions into your merchant bank account minus credit card <a href="https://www.fool.com/the-ascent/research/average-credit-card-processing-fees-costs-america/">processing fees</a>. At that point, your role as merchant in the process is complete.</p>
						<p>Both the acquiring and issuing banks will continue to work until the money is moved. The issuing bank pays the acquiring bank for the cardholders' transaction. The cardholder pays the issuing bank. All this happens without the involvement of your business.</p>
						<ul>
							<li><span class="no">1. Merchant:</span>  Business owners start settlement by submitting a batch of approved authorizations to the acquiring bank at the end of each business day. The batches include any number of individual authorizations and transaction information.</li>
							<li><span class="no">2. Acquiring bank:</span>  The acquiring bank reconciles and submits the batch of authorizations over the appropriate card's network like Banknet or Visanet. Funds are deposited into the merchant bank account for you by the acquiring bank through the automated clearinghouse (ACH). This is minus any processing fees charged either daily or monthly or both, depending on the agreement you have for merchant processing. <a href="https://salequick.com/pricing">SaleQuick customers</a>, for example, only <a href="https://www.merchantmaverick.com/flat-rate-credit-card-processing/">pay one flat rate</a> for their merchant processing.</li>
							<li><span class="no">3. Credit card network:</span>  The card association then debits the issuing bank's account (that of the cardholder) and credits those funds to the acquiring bank's account for the authorizations' net amount minus any interchange and network fees.</li>
							<li><span class="no">4. Issuing bank:</span>  The bank that issued the card pays the acquiring bank for its cardholders' purchases.</li>
							<li><span class="no">5. Cardholder:</span>  It's the responsibility of the customer to repay the issuing bank for their purchases. They also have to pay any interest or fees associated with their card agreement.</li>
						</ul>
						<h2 class="h4">Fees and Funds</h2>
						<p>The processor deposits the money from your credit card sales in your merchant bank account and takes out any processing fees. There are different ways processors assess fees and deposit funds into your merchant account.</p>
						<ul>
							<li><span class="no">1. Credit card deposits:</span>  As long as you batch your transactions by a certain time, most processors will deposit the funds from your sale the next day. If you miss the cutoff time, you won't get the funds until the next business day if you make the cutoff time that day. Processors will place holds on any funds from a transaction that they deem suspect. Typically, they'll let you know exactly when the funds will be received if the transaction is legitimate.</li>
							<li><span class="no">2. Fee deduction discounting:</span>  Processors deduct credit card fees from your transactions in one of two ways: daily or monthly discounting. Daily discounting means the processor deducts fees as agreed to from your funds each day before depositing them in your merchant account. Each day using this method, you receive the net sale amount each day minus any fees. With monthly discounting, the processor deducts its processing fees for the whole month once a month. They deposit funds daily though so you receive the gross sale amount before fees each day.</li>
						</ul>
						<p>There are advantages and disadvantages to each method. Many processors allow you to select which method you'd like for your small business.</p>
