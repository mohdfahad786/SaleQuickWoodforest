						<p>Choosing the right payment provider for your business isn't an easy task. Each processor comes with its own contracts and fees. To find the best deal, it's important to take some time to weigh the pros and cons of each provider you're considering. Do your homework.</p>
						<p>The following eight questions should be asked of any payment provider you're considering. </p>
						<h2 class="h4">1. Are there distinct fees or rates connected with different types of cards?</h2>
						<p>Many providers charge different fees for processing the transactions of different card types like business, personal, and rewards cards. There may even be different fees for transaction amounts, different industries, and more. It's important to know exactly what you'll be paying for each transaction because it can have a huge impact on your bottom line.</p>
						<p>What would you pay per transaction at <a href="https://salequick.com/">SaleQuick</a>? SaleQuick offers a better system that's affordable and easy to use. Our billing platform uses flat rate processing. That means you pay one discount rate for our service. </p>
						<ul>
							<li>No matter what types of transactions your business uses.</li>
							<li>No extra fees for accepting credit, corporate, debit,  and reward cards.</li>
							<li>No matter the size or volume of your transactions.</li>
						</ul>
						<p>Your business pays one discount rate for our service. That's all.</p>
						<h2 class="h4">2. Do you bill for "chargebacks"? Or are all charges relevant to a transaction billed in the same month?</h2>
						<p>Many payment providers will advertise low rates to get you to sign up. Look a little closer. Often those low rates only apply to certain card types. Then you find out you didn't get that rate for all your other transactions that month.</p>
						<p>Let's say for the month of March, you believed you would be getting a rate of 1.8% on your credit card transactions and that includes all the rewards cards you processed. While all the transactions were processed in March, when April arrives your provider may charge you back at a higher rate. That creates a problem because you have two separate statements with two different rates for the same transactions. How do you figure out how much you owe exactly? How much time will it take to figure that out?</p>
						<p>With <a href="https://salequick.com/pricing">SaleQuick</a>, you'll never be in that situation. You still only pay one flat rate for our processing services. No chargebacks. There are no setup fees or monthly fees either. Just one, easy-to-understand fee and no surprises.</p>
						<h2 class="h4">3. What fees are charged when I accept credit cards online, manually enter them, and swipe them?</h2>
						<p>Many payment providers charge different rates depending on how payments are processed. It's explained that they do this to compensate for their fraud risk. Since there's allegedly less chance of fraud when cards are physically swiped and identification verified (you check ID each time, right?), transactions processed that way often get a lower rate.</p>
						<p>There's a higher fee for cards entered manually. If someone calls you to place an order and gives you their number over the phone, there's a chance that it could be a fraudulent transaction. The rate for those will be higher.</p>
						<p>Online transactions are considered the riskiest of all and are often given the highest processing rate. The higher rate, again, is to help providers cover their risk.</p>
						<p>It's important to know about how many of each type of transaction you use on average each month. Then it's good to know exactly what the rates are for each type of transaction processing for any provider you're considering.</p>
						<p>SaleQuick only charges one flat rate per month. It doesn't matter what types of transactions your business uses. It's one less thing to worry about when you choose us as your payment provider.</p>
						<h2 class="h4">4. Is there a separate fee for your gateway?</h2>
						<p>It's not an uncommon practice for payment providers to charge merchants a separate fee for their payment gateway. Usually, the charge is assigned for each transaction. On top of your standard fee, something like 2.9% + $0.31, you'd pay the required gateway fee for each transaction. It's easy to see how fast all those small fees can add up and take away from your bottom line.</p>
						<p>With SaleQuick, there are no extra fees for gateways or anything else. Just one flat discount rate for everything. It doesn't get any easier than that.</p>
						<h2 class="h4">5. What fees are charged each month?</h2>
						<p>This is where it's important to read the fine print. Many providers advertise reasonable upfront monthly fees. On closer inspection, you may find extra fees hidden in the contract. They can be fees for batch processing, transfer fees for moving funds to your bank account from a merchant account, statement fees, and many more. Some of these extra, vague fees might be temporarily waived for a time as part of a promotion to get you to sign up. It's not uncommon for them to be added back later once the promotion is over.</p>
						<p>There's no guesswork at SaleQuick. We pride ourselves on offering a reputable flat rate platform. The merchants we serve pay only one easy-to-understand fee with no surprises or extra, vague fees added. At SaleQuick, pricing transparency is important.</p>
						<h2 class="h4">6. Is there a minimum processing fee each month?</h2>
						<p>Many providers require you to have a certain number of transactions each month. If your volume drops below that number, they'll charge you a monthly minimum fee. For newer businesses, this can be a significant expense until you're established.</p>
						<p>At SaleQuick, it doesn't matter how many transactions you have or the size of those transactions. We don't charge you an extra fee each month if you don't have a certain volume. Period.</p>
						<h2 class="h4">7. Are there any limits on how much a merchant can process with your service?</h2>
						<p>Nothing can be more frustrating to a merchant than having a provider-imposed limit on how many transactions they can process. You're trying to build and grow your business. That's no time for limits. When you hit your busy season? It can become a nightmare. And on top of all that, what would it do to your reputation? You shouldn't have to tell customers that you can't take their card because you've hit your transaction limit for the month.</p>
						<p>At SaleQuick, we want your business to grow. We want you to be successful. We don't impose limits on how many transactions you process. The limit doesn't exist.</p>
						<h2 class="h4">8. What type of customer support do you provide?</h2>
						<p>When you need help, will it be available to you? The answer is an important one. There's nothing worse than having your payment processing go down with a line of people waiting. The provider you work with needs to offer live customer support. Automated phone systems won't cut it here. Often, with some providers, it's a trade-off. Maybe you got a good low rate, but customer service is either unavailable or inadequate.</p>
						<p>At SaleQuick, we're here when you need us most. Support is one of the things we do best. Our support team is available to you 24 hours a day to keep your business up and running.</p>
						<p>Finding the right payment provider isn't an easy task. Now that you know the best questions to ask, gather your facts. Then you can make comparisons between the providers you're considering quickly.  </p>
